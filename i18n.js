function t(guildLang, key, ...args) {
  const lang   = translations[guildLang] ?? translations.es;
  const fallback = translations.es;
  const value  = lang[key] ?? fallback[key];
  if (!value) return `[${key}]`;
  return typeof value === 'function' ? value(...args) : value;
}

